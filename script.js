// Function to render posts on the Explore page
function renderExplorePosts() {
    const exploreContainer = document.getElementById('explore-container');

    exploreData.forEach(postData => {
        const postElement = document.createElement('div');
        postElement.classList.add('post');

        const imageElement = document.createElement('img');
        imageElement.src = postData.imageUrl;

        const captionElement = document.createElement('p');
        captionElement.textContent = postData.caption;

        postElement.appendChild(imageElement);
        postElement.appendChild(captionElement);
        exploreContainer.appendChild(postElement);
    });
}

